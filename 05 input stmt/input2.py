
#Accepting 2 values from the user keyboard and performing addition
#Note:whatever we are accepting through input function,by default taken
#     as string and later we need to typecast(convert) to required type
x=int(input("Enter value of x:"))
y=int(input("Enter value of y:"))
print(x,type(x))
print(y,type(y))
print(x+y)

