#complex :For creating complex numbers

#Complex numbers are comprised of 2 parts
#1.Real part --->float
#2.Imaginary part->float

x=3+4j

print(x)
print(type(x))

print(x.real)
print(x.imag)

#A Complex number can also be created using complex() function
x=complex(3,-5)
print(x,type(x))
#ex:2
y=complex(0.3,0.4)
print(y,type(y))

#ex:3
z=complex(3)
print(z,type(z))

#ex:4
w=complex('4-6j')
print(w)











